/*
Method Overloading = Compile-Time Polymorphism
What it is: Multiple methods in the same class with the same name but different parameter lists (number or type of parameters).
Why it's compile-time: The method to call is determined by the compiler, based on the method signature.
  
Method Overriding = Run-Time Polymorphism
What it is: A child class provides a specific implementation of a method that is already defined in its parent class.
Why it's runtime: The method to call is determined at runtime, using dynamic dispatch (via virtual functions in C++).

*/
